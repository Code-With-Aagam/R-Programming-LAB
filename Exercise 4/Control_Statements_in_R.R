# 1.Conditional Grading System

score <- scan(n=1)
if(score>=90){
  print(paste("A"))
}else if(score>=80){
  print(paste("B"))
}else if(score>=70){
  print(paste("C"))
}else if(score>=60){
  print(paste("D"))
}else{
  print(paste("F"))
}

